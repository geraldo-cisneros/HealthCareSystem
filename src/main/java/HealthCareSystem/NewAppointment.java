    private void submitNewApptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitNewApptActionPerformed
        // TODO add your handling code here:
            String first = pFName.getText();
            String last = pLName.getText();
            String bd = BirthDate.getText();
            String doc = docList.getSelectedItem().toString();
            String d = date.getText();
            String t = time.getText();
            String appFile = "src/main/java/HealthCareSystem/appointments.txt";
            String docFile = "src/main/java/HealthCareSystem/docAvailability.txt";
            String type = "Appointment";
            String data = first + " " + last + " " + bd + " " + doc + " "+ d + " " + t + "\n";
            
            Boolean available = UpdateAppointment.docAvail(doc, d, t);
            
            if(!available){
                writeToFile(appFile, data, type, true);
                data = doc + " "+ d + " " + t;
                writeToFile(docFile, data, type, true); 
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null, "Doctor is not available at that time and date.");
            }
    }//GEN-LAST:event_submitNewApptActionPerformed

